void main() {
  String grade = 'h';
  switch (grade) {
    case 'A':
      print("exellent");
      break;
    case 'B':
      print("Very Good");
      break;
    case 'C':
      print("Average");
      break;
    case 'D':
      print("Fail");
      break;
    default:
      print("invalid grade entry");
      break;
  }
}
