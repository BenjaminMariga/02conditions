void main() {
  //simple if else if to display colours of the kenyan flag
  String colour = "red";
  if (colour == "red") {
    print("blood");
  } else if (colour == "white") {
    print("peace");
  } else if (colour == "black") {
    print("people");
  } else if (colour == "green") {
    print("vegetation");
  } else {
    print("invalid entry");
  }
}
